package chapter01;
public class switchj {
	public static void main(String[] args) {
	int week=8;
	switch (week){
	case 1:System.out.println("星期一");
	break;
	case 2:System.out.printf("星期二");
	break;
	case 3:System.out.printf("星期三");
	break;
	case 4:System.out.printf("星期四");
	break;
	case 5:System.out.printf("星期五");
	break;
	case 6:System.out.printf("星期六");
	break;
	case 7:System.out.printf("星期七");
	break;
	default:System.out.printf("输入错误");
	}
	}
}


